package com.sail.sailright2new;

public class Course {

    String courseName, courseRoute, courseRounding;

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public String getCourseRoute() {
        return courseRoute;
    }

    public void setCourseRoute(String courseRoute) {
        this.courseRoute = courseRoute;
    }

    public String getCourseRounding() { return courseRounding; }

    public void setCourseRounding(String courseRounding) { this.courseRounding = courseRounding; }
}
